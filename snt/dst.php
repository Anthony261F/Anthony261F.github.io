					<h2 id="DonnéeStructurées2">Les Données Structurées</h2>
					<h4>Une donnée est une valeur décrivant un objet, elle est personnelle lorsqu'elle se rapporte à une personne. Ces données sont stockés dans des fichiers qui sont stockées dans des bases de données.</h4><h4>Les métadonnées sont des données suplémantaires qui permettent de décrire le fichier.</h4><h4>De nombreux traitements,sur une ou plusieurs collections,sont possibles: l'indexation, les tris, la recherche, les filtres, les calculs, la visualisation. Ces opérations permettent notamment de réaliser des statistiques, d'étudier des évolutions.</h4>
				</div>
