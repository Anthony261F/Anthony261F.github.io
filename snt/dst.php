					<h2 id="MétadonnéesFichier">Les Métadonnées d'un Fichier</h2>
					<h4 id="souligne">Definition :</h4><h4>Ensemble de données décrivant les caractéristiques d'un fichier, sans être spécifiquement liées à son contenu.</h4>
					<h4 id="souligne">En d'autre thermes :</h4><h4>Ce sont les informations qui décrivent un fichier. Par exemple, une fois que l'on a saisi toutes les infromations des élèves de la classe dans un tableur, le nom, la date de création ou le type de fichier constitueraient des métadonnées.</h4>
