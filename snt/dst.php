				<div id="block-gauche">
					<h2>Une Base De Donnée</h2>
					<h4 id="souligne">Définiton :</h4><h4>Entité stockant des données structurées et permettant leur traitement à l'aide d'algorithmes.</h4>
					<h4 id="souligne">En d'autres termes :</h4><h4>Une base de données est une sorte de réservoir de données que l'on peut récupérer et étudier.Le secrétariat d'un lycée collecte et utilise ainsi de nombreuses informations qui concernant les élèves, les professeurs, les classes,etc. Toutes ces données constituent la base de données de l'établissement.</h4>
				</div>
				
				<div id="block-droite">
					<h2>Une Donnée</h2>
					<h4 id="souligne">Définiton :</h4><h4>Valeur ou ensemble de valeurs pouvant prendre plusieurs formes (nombres,chaînes de caractères) et permettant de décrire un ou plusieurs objets.</h4>
					<h4 id="souligne">En d'autres termes :</h4><h4>C'est une information sur un objet ou une personne. Pour une classe d'élèves de seconde, une donnée pourrait ainsi correspondre au prénom d'un élève.</h4>
				</div>
				<br style="clear: both;">
				
				<div id="block-gauche">
					<h2>Le cloud</h2>
					<h4 id="souligne">Définition :</h4><h4>Service de stockage de données à distance via internet.</h4>
					<h4 id="souligne">En d'autres termes :</h4><h4>Le cloud est un service qui permet de sauvegarder et d'accéder à des dossiers sur internet. Le cloud, ou nuage, peut être associé à un vaste espace dans lequel les données informatiques sont envoyées sans que l'on sache vraiment où elles se trouvent. C'est ce qui permet aux enseignants de faire l'appel en ligne par exemple.</h4>
				</div>
				
				<div id="block-droite">
					<h2>Les Données Structurées</h2>
					<h4 id="souligne">Définition :</h4><h4>Ensemble de données stockées sous la forme d'une table regroupant de multiples objets selon un ou plusieurs descripteurs.</h4>
					<h4 id="souligne">En d'autres termes :</h4><h4>Les données peuvent être structurées en tableau regroupant des informations sur plusieurs objets selon leurs caractéristiques. Pour la classe de seconde, il peut ainsi s'agir d'un tableau regroupant plusieurs informations sur les élèves de la classe, comme leur prénom, leur nom, leur date de naissance, etc.</h4>
				</div>
				<br style="clear: both;">
				
				<div id="block-centre">
					<h2>Les Métadonnées d'un Fichier</h2>
					<h4 id="souligne">Definition :</h4><h4>Ensemble de données décrivant les caractéristiques d'un fichier, sans être spécifiquement liées à son contenu.</h4>
					<h4 id="souligne">En d'autre thermes :</h4><h4>Ce sont les informations qui décrivent un fichier. Par exemple, une fois que l'on a saisi toutes les infromations des élèves de la classe dans un tableur, le nom, la date de création ou le type de fichier constitueraient des métadonnées.</h4>
				</div>
			</div>
			<br style="clear: both;">
			<div>
				<br><br><br>
				<h1 id="titre">Cours</h1>
				<br>
				<div id="block-centre">
					<h2>Les Données Structurées</h2>
					<h4>Une donnée est une valeur décrivant un objet, elle est personnelle lorsqu'elle se rapporte à une personne. Ces données sont stockés dans des fichiers qui sont stockées dans des bases de données.</h4><h4>Les métadonnées sont des données suplémantaires qui permettent de décrire le fichier.</h4><h4>De nombreux traitements,sur une ou plusieurs collections,sont possibles: l'indexation, les tris, la recherche, les filtres, les calculs, la visualisation. Ces opérations permettent notamment de réaliser des statistiques, d'étudier des évolutions.</h4>
				</div>
				<div id="block-centre">
					<h2>Le Stockage des Données</h2>
					<h4>Les fichiers de données sont stockés sur des supports de stockages informatiques : ces stockage inforormatique peuvent etre internes(disuqe dur ou disque SSD) ou externes (disque dur externe, clé USB, CD-ROM, DVD-ROM, carte SD, etc). Il est nécessaire ded réaliser des sauvegardes régulières et sur différents suypports car les données peuvent facilement être corrompues ou détruites.</h4><h4>Le stockage en ligne, ou dans le cloud, consiste à placer des documents et des fichiers dans une espace accessible via internet. Toutes ces données sont sauvegardées sur des serveurs distants et il est possible d'y avoir accès depuis n'importe quel appareil connecté à Internet.</h4>
