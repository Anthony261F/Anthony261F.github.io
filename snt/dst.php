					<h2 id="DonnéeStructurées">Les Données Structurées</h2>
					<h4 id="souligne">Définition :</h4><h4>Ensemble de données stockées sous la forme d'une table regroupant de multiples objets selon un ou plusieurs descripteurs.</h4>
					<h4 id="souligne">En d'autres termes :</h4><h4>Les données peuvent être structurées en tableau regroupant des informations sur plusieurs objets selon leurs caractéristiques. Pour la classe de seconde, il peut ainsi s'agir d'un tableau regroupant plusieurs informations sur les élèves de la classe, comme leur prénom, leur nom, leur date de naissance, etc.</h4>
				</div>
				<br style="clear: both;">
				
