					<h2 id="StockageDonnée">Le Stockage des Données</h2>
					<h4>Les fichiers de données sont stockés sur des supports de stockages informatiques : ces stockage inforormatique peuvent etre internes(disuqe dur ou disque SSD) ou externes (disque dur externe, clé USB, CD-ROM, DVD-ROM, carte SD, etc). Il est nécessaire ded réaliser des sauvegardes régulières et sur différents suypports car les données peuvent facilement être corrompues ou détruites.</h4><h4>Le stockage en ligne, ou dans le cloud, consiste à placer des documents et des fichiers dans une espace accessible via internet. Toutes ces données sont sauvegardées sur des serveurs distants et il est possible d'y avoir accès depuis n'importe quel appareil connecté à Internet.</h4>
