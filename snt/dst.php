					<h2 id="Cloud">Le cloud</h2>
					<h4 id="souligne">Définition :</h4><h4>Service de stockage de données à distance via internet.</h4>
					<h4 id="souligne">En d'autres termes :</h4><h4>Le cloud est un service qui permet de sauvegarder et d'accéder à des dossiers sur internet. Le cloud, ou nuage, peut être associé à un vaste espace dans lequel les données informatiques sont envoyées sans que l'on sache vraiment où elles se trouvent. C'est ce qui permet aux enseignants de faire l'appel en ligne par exemple.</h4>
